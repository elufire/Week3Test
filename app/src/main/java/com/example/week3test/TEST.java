    public static boolean isOutbreak(Room[][] buildingTemp) {
        Room [] floor;

        for(int i=0; i<buildingTemp.length; i++){

            floor = buildingTemp[i];

            for(int j=0; j<floor.length; j++){
                int [][] array = new int[buildingTemp.length][buildingTemp[0].length];
                if(buildingTemp[i][j].isInfected){
                    //System.out.println(getNumberOfConnections(buildingTemp, i, j));
                    if(getNumberOfConnections(buildingTemp, array,  i, j)>=4){
                        return true;
                    }

                }
            }

        }
        return false;
    }

    public static int getNumberOfConnections(Room[][] tempBuild, int [][] array, int a, int b){






        if(tempBuild[a-1][b].isInfected && array[a-1][b] != 1){
            array[a-1][b] = 1;
            return getNumberOfConnections(tempBuild, array, a-1, b) +1;

        }



        if(tempBuild[a][b-1].isInfected && array[a][b-1] != 1){
            array[a][b-1] = 1;
            return getNumberOfConnections(tempBuild, array, a, b-1) +1;

        }

        if(tempBuild[a+1][b].isInfected && array[a+1][b] != 1){
            array[a+1][b] = 1;
            return getNumberOfConnections(tempBuild, array, a+1, b) +1;

        }

        if(tempBuild[a][b+1].isInfected && array[a][b+1] != 1){
            array[a][b+1] = 1;
            return getNumberOfConnections(tempBuild, array, a,b+1) +1;

        }



        return 0;

    }
